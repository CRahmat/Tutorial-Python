#basic Function
def functionPenjumlahan(angka1,angka2):
    print("Hasil Penjumlahan ",angka1,"+ ",angka2, "adalah ",angka1+angka2)
def functionPengurangan(angka1,angka2):
    print("Hasil Pengurangan ",angka1,"+ ",angka2, "adalah ",angka1-angka2)
def functionPerkalian(angka1,angka2):
    print("Hasil Perkalian ",angka1,"+ ",angka2, "adalah ",angka1*angka2)
def functionPembagian(angka1,angka2):
    print("Hasil Pembagian ",angka1,"+ ",angka2, "adalah ",angka1/angka2)

#run Function
functionPenjumlahan(10,20)
functionPengurangan(20,10)
functionPerkalian(10,20)
functionPembagian(20,10)